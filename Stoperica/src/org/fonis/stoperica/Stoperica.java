    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {
        if (t.isRunning() && zvuk) {
            zvuk = false;
            song.stop();
            jButton5.setIcon(mute);
        } else if (zvuk) {
            zvuk = false;

            jButton5.setIcon(mute);
        } else if (t.isRunning()) {
            zvuk = true;
            song.loop();
            jButton5.setIcon(unmute);
        } else {
            zvuk = true;
            jButton5.setIcon(unmute);
        }
    }

