    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {

        t.start();
        if (zvuk) {
            try {

                songPath = getClass().getResource(melodija);


                song = Applet.newAudioClip(songPath);
            } catch (Exception e) {
            }
            song.loop();
        }
    }

